#!/usr/bin/env node
/**
 * Regenerates:
 *   - support/article-urls.txt
 *   - support/intercom-knowledge-seed.html
 *   - prints sitemap.xml <url> lines for copy-paste if needed
 *
 * Run from repo root: node scripts/generate-intercom-knowledge.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');
const BASE = 'https://harve.ai';

function collectUrls() {
  const urls = new Set();

  const supportHtml = path.join(ROOT, 'support.html');
  if (fs.existsSync(supportHtml)) urls.add(`${BASE}/support`);

  const supportDir = path.join(ROOT, 'support');
  for (const name of fs.readdirSync(supportDir)) {
    if (!name.endsWith('.html') || name === 'intercom-knowledge-seed.html') continue;
    const slug = name.replace(/\.html$/, '');
    urls.add(`${BASE}/support/${slug}`);
  }

  const articlesDir = path.join(supportDir, 'articles');
  if (fs.existsSync(articlesDir)) {
    for (const name of fs.readdirSync(articlesDir)) {
      if (!name.endsWith('.html')) continue;
      const slug = name.replace(/\.html$/, '');
      urls.add(`${BASE}/support/articles/${slug}`);
    }
  }

  return [...urls].sort((a, b) => a.localeCompare(b));
}

function buildSeedHtml(urls) {
  const items = urls
    .map((u) => {
      const label = u.replace(`${BASE}/`, '').replace(/^\//, '') || 'home';
      return `      <li><a href="${u}">${label}</a></li>`;
    })
    .join('\n');

  return `<!doctype html>
<html lang="en">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<title>Harve Help — Knowledge index (for Intercom / crawlers)</title>
<meta name="robots" content="noindex, follow">
<link rel="icon" type="image/svg+xml" href="/favicon.svg" />
<link rel="stylesheet" href="support.css">
</head>
<body>
<header class="hc-top">
  <a class="logo" href="/support">Harve</a>
  <nav class="hc-nav"><a href="/support">Help Center</a></nav>
</header>
<main class="article-shell" id="intercom-knowledge-seed">
  <p class="article-lead" style="margin-bottom:1rem">
    This page lists every public help URL. Intercom Fin and other crawlers can start here to discover all articles.
    Humans: use the <a href="/support">Help Center</a> instead.
  </p>
  <ul class="intercom-seed-list">
${items}
  </ul>
</main>
</body>
</html>
`;
}

function main() {
  const urls = collectUrls();
  const txt = [
    '# Auto-generated by scripts/generate-intercom-knowledge.mjs',
    '# Canonical production URLs (clean paths, Vercel rewrites).',
    '',
    ...urls,
    '',
  ].join('\n');

  fs.writeFileSync(path.join(ROOT, 'support', 'article-urls.txt'), txt, 'utf8');

  const seedPath = path.join(ROOT, 'support', 'intercom-knowledge-seed.html');
  fs.writeFileSync(seedPath, buildSeedHtml(urls), 'utf8');

  console.log(`Wrote ${urls.length} URLs to support/article-urls.txt`);
  console.log(`Wrote support/intercom-knowledge-seed.html`);
  console.log('\nIntercom one-shot URL (add as Website source or “Additional URLs”):\n');
  console.log(`  ${BASE}/support/intercom-knowledge-seed\n`);
}

main();
